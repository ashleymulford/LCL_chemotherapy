write.table(CEU_dauno_pheno, "/home/ashley/LCL_chemotherapy/CEU/CEU_dauno_pheno.txt", col.names = F, row.names = F, sep = "\t", quote = F)
write.table(YRI_etop_pheno, "/home/ashley/LCL_chemotherapy/YRI/YRI_etop_pheno.txt", col.names = F, row.names = F, sep = "\t", quote = F)
