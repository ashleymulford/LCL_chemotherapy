#Import necessary libraries
library(data.table)
library(dpylr)

#Read in master pheno file and subset by pop
master_phenos <- read.table("/home/ashley/LCL_chemotherapy/master_cytotoxicity_info_130311_plus_igrowth.txt", header = T)
YRI_phenos <- subset(master_phenos, pop == "YRI")
CEU_phenos <- subset(master_phenos, pop == "CEU")
CHB_phenos <- subset(master_phenos, pop == "CHB")
JPT_phenos <- subset(master_phenos, pop == "JPT")
ASN_phenos <- full_join(JPT_phenos, CHB_phenos)

#Read in fam file for each pop and set column names
YRI_fam <- fread("/home/ashley/LCL_chemotherapy/YRI/YRI.chr10.TGP_and_imputed.20130416.exclude_BAD_SNPs.fam")
CEU_fam <- fread("/home/ashley/LCL_chemotherapy/CEU/CEU.chr10.TGP_and_imputed.20130416.exclude_BAD_SNPs.fam")
ASN_fam <- fread("/home/ashley/LCL_chemotherapy/ASN/ASN.chr10.TGP_and_imputed.20130718.phased.R2gt0.8.fam")
colnames(YRI_fam) <- c("FID", "IID", "FatID", "MatID", "Sex", "Pheno")
colnames(CEU_fam) <- c("FID", "IID", "FatID", "MatID", "Sex", "Pheno")
colnames(ASN_fam) <- c("FID", "IID", "FatID", "MatID", "Sex", "Pheno")

#Join pheno with with fam, keeping row order consistent with that of fam
YRI_phenos_ordered <- left_join(YRI_fam, YRI_phenos, by = c("FID" = "FID", "IID" = "IID"))
CEU_phenos_ordered <- left_join(CEU_fam, CEU_phenos, by = c("FID" = "FID", "IID" = "IID"))
ASN_phenos_ordered <- left_join(ASN_fam, ASN_phenos, by = c("FID" = "FID", "IID" = "IID"))

#Create single-column pheno data frames
YRI_carbo_pheno<-select(YRI_phenos_ordered, contains("CARBO_RnIC50"))
CEU_carbo_pheno<-select(CEU_phenos_ordered, contains("CARBO_RnIC50"))
ASN_carbo_pheno<-select(ASN_phenos_ordered, contains("CARBO_RnIC50"))
YRI_arac_pheno<-select(YRI_phenos_ordered, contains("ARAC_RnAUC"))
CEU_arac_pheno<-select(CEU_phenos_ordered, contains("ARAC_RnAUC"))
ASN_arac_pheno<-select(ASN_phenos_ordered, contains("ARAC_RnAUC"))
YRI_cis_pheno<-select(YRI_phenos_ordered, contains("CIS_RnIC50"))
CEU_cis_pheno<-select(CEU_phenos_ordered, contains("CIS_RnIC50"))
ASN_cis_pheno<-select(ASN_phenos_ordered, contains("CIS_RnIC50"))
YRI_cape_pheno<-select(YRI_phenos_ordered, contains("CAPE_RnAUC"))
CEU_cape_pheno<-select(CEU_phenos_ordered, contains("CAPE_RnAUC"))
ASN_cape_pheno<-select(ASN_phenos_ordered, contains("CAPE_RnAUC"))
YRI_etop_pheno<-select(YRI_phenos_ordered, contains("ETOP_Rn_IC50"))
CEU_etop_pheno<-select(CEU_phenos_ordered, contains("ETOP_Rn_IC50"))
YRI_pacl_pheno<-select(YRI_phenos_ordered, contains("PACL_Rn_AUC"))
CEU_pacl_pheno<-select(CEU_phenos_ordered, contains("PACL_Rn_AUC"))
YRI_dauno_pheno<-select(YRI_phenos_ordered, contains("DAUNO_RnIC50"))
CEU_dauno_pheno<-select(CEU_phenos_ordered, contains("DAUNO_RnIC50"))
YRI_peme_pheno<-select(YRI_phenos_ordered, contains("PEME_RnAUC"))
CEU_peme_pheno<-select(CEU_phenos_ordered, contains("PEME_RnAUC"))

#Output pheno data frames
